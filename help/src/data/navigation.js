export function findArticle(categorySlug, articleSlug) {
  const category = navigation.find((c) => c.slug === categorySlug)
  if (!category) return null
  if (!articleSlug) {
    return { category, item: category.items[0] }
  }
  const item = category.items.find((i) => i.slug === articleSlug)
  return item ? { category, item } : null
}
